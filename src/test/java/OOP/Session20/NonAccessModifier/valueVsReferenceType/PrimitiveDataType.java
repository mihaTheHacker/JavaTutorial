package OOP.Session20.NonAccessModifier.valueVsReferenceType;

public class PrimitiveDataType {

    //-128 pana la 127
    byte myByte = 12;
    //16 bit integer value
    //-32768 pana la 32767
    short myShort = 1234;
    //32 bit integer value
    //-2147483648 pana la 2147483647
    int myInt = 12345;
    //64 bit integer value
    long myLong = 123456789L;
    //64 bit integer value
    double myDouble = 13.89;
    //64 bit floating point value
    //about 15 decimal digits of precision
    float myFloat = 123456789.123456789f;
    //boolean value
    boolean myBoolean = true;
    boolean myTestBoolean = (myInt > 200);
    //character value
    char myChar = 'A';

}
